    public void gettAllMitarbeiter() {
        ArrayList<Benutzer> mitarbeiterList = VerwaltungHandler.gettAllMitarbeiter();
        DefaultTableModel tm = (DefaultTableModel) jTableMitarbeiter.getModel();
        tm.setRowCount(0);

        for (Benutzer b : mitarbeiterList) {
            Object[] rowData = {
                b.getBenutzerId(),
                b.getBenutzerVollName(),
                b.getRolle(),
                b.getMail(),
                b.getTelNummer(),
                b.getBenutzerName(),
                b.getPasswort()
            };
            tm.addRow(rowData);
        }
    }

