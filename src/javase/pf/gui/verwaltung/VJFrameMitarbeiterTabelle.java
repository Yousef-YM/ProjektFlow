    private void jButtonBenutzerHinzufuegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBenutzerHinzufuegenActionPerformed
        String vollName = jTextFieldVollName.getText();
        String rolle = jTextFieldRolle.getText();
        String mail = jTextFieldMail.getText();
        String phone = jTextFieldTelNummer.getText();
        String benutzerName = jTextFieldBenutzerName.getText();
        String passwort = jTextFieldPasswort.getText();

        Benutzer benutzer = new Benutzer(vollName, rolle, mail,
                phone, benutzerName, passwort);
        VerwaltungHandler.benutzerHinzufuegen(benutzer);

        gettAllMitarbeiter();
        textFieldLeer();
        JOptionPane.showMessageDialog(null, "Mitarbeiter erfolgreich hinzufügt");
    }//GEN-LAST:event_jButtonBenutzerHinzufuegenActionPerformed

    private void jButtonBearbeitenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBearbeitenActionPerformed
        int benutzerId = Integer.parseInt(jTextFieldBenutzerId.getText());
        String vollName = jTextFieldVollName.getText();
        String rolle = jTextFieldRolle.getText();
        String mail = jTextFieldMail.getText();
        String phone = jTextFieldTelNummer.getText();
        String benutzerName = jTextFieldBenutzerName.getText();
        String passwort = jTextFieldPasswort.getText();

        Benutzer benutzer = new Benutzer(benutzerId, vollName, rolle, mail,
                phone, benutzerName, passwort);
        VerwaltungHandler.benutzerBearbeiten(benutzer);

        DefaultTableModel tm = (DefaultTableModel) jTableMitarbeiter.getModel();

        tm.setValueAt(vollName, jTableMitarbeiter.getSelectedRow(), 1);
        tm.setValueAt(rolle, jTableMitarbeiter.getSelectedRow(), 2);
        tm.setValueAt(mail, jTableMitarbeiter.getSelectedRow(), 3);
        tm.setValueAt(phone, jTableMitarbeiter.getSelectedRow(), 4);
        tm.setValueAt(benutzerName, jTableMitarbeiter.getSelectedRow(), 5);
        tm.setValueAt(passwort, jTableMitarbeiter.getSelectedRow(), 6);

        gettAllMitarbeiter();
        textFieldLeer();
        JOptionPane.showMessageDialog(null, "Daten sind erfolgreich bearbeitet");
    }//GEN-LAST:event_jButtonBearbeitenActionPerformed


    private void jTableMitarbeiterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMitarbeiterMouseClicked
        DefaultTableModel tm = (DefaultTableModel) jTableMitarbeiter.getModel();

        int benutzerId = (int) tm.getValueAt(jTableMitarbeiter.getSelectedRow(), 0);
        String vollName = (String) tm.getValueAt(jTableMitarbeiter.getSelectedRow(), 1);
        String rolle = (String) tm.getValueAt(jTableMitarbeiter.getSelectedRow(), 2);
        String mail = (String) tm.getValueAt(jTableMitarbeiter.getSelectedRow(), 3);
        String phone = (String) tm.getValueAt(jTableMitarbeiter.getSelectedRow(), 4);
        String benutzername = (String) tm.getValueAt(jTableMitarbeiter.getSelectedRow(), 5);
        String passwort = (String) tm.getValueAt(jTableMitarbeiter.getSelectedRow(), 6);

        jTextFieldBenutzerId.setText(String.valueOf(benutzerId));
        jTextFieldVollName.setText(vollName);
        jTextFieldRolle.setText(rolle);
        jTextFieldMail.setText(mail);
        jTextFieldTelNummer.setText(phone);
        jTextFieldBenutzerName.setText(benutzername);
        jTextFieldPasswort.setText(passwort);

    }//GEN-LAST:event_jTableMitarbeiterMouseClicked

    private void jButtonBenutzerLoeschenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBenutzerLoeschenActionPerformed
        int benutzerId = Integer.parseInt(jTextFieldBenutzerId.getText());
        VerwaltungHandler.benutzerLoeschen(benutzerId);

        gettAllMitarbeiter();
        textFieldLeer();
        JOptionPane.showMessageDialog(null, "Daten sind gelöscht");
    }//GEN-LAST:event_jButtonBenutzerLoeschenActionPerformed

